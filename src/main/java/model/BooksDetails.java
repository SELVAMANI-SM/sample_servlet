package model;

public class BooksDetails {
	private String bookId;
	private String bookName;
	private String bookAuthor;
	private String bookDepartment;
	private String bookStatus;
	private String eBookLink;
	private String bookVideoLink;
	private String bookImageLink;
	public String getBookImageLink() {
		return bookImageLink;
	}
	public void setBookImageLink(String bookImageLink) {
		this.bookImageLink = bookImageLink;
	}
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	public String getBookDepartment() {
		return bookDepartment;
	}
	public void setBookDepartment(String bookDepartment) {
		this.bookDepartment = bookDepartment;
	}
	public String getBookStatus() {
		return bookStatus;
	}
	public void setBookStatus(String bookStatus) {
		this.bookStatus = bookStatus;
	}
	public String geteBookLink() {
		return eBookLink;
	}
	public void seteBookLink(String eBookLink) {
		this.eBookLink = eBookLink;
	}
	public String getBookVideoLink() {
		return bookVideoLink;
	}
	public void setBookVideoLink(String bookVideoLink) {
		this.bookVideoLink = bookVideoLink;
	}
	


}
