package model;

public class NewsPaperDetails {
private String dinamalarNewsPaper;
private String hinduNewsPaper;
private String indianExpressNewsPaper;
public String getDinamalarNewsPaper() {
	return dinamalarNewsPaper;
}
public void setDinamalarNewsPaper(String dinamalarNewsPaper) {
	this.dinamalarNewsPaper = dinamalarNewsPaper;
}
public String getHinduNewsPaper() {
	return hinduNewsPaper;
}
public void setHinduNewsPaper(String hinduNewsPaper) {
	this.hinduNewsPaper = hinduNewsPaper;
}
public String getIndianExpressNewsPaper() {
	return indianExpressNewsPaper;
}
public void setIndianExpressNewsPaper(String indianExpressNewsPaper) {
	this.indianExpressNewsPaper = indianExpressNewsPaper;
}

}
